public class Item {
    private int imgId;
    private String names;

    public Item() {
    }

    public Item(int imgId, String names) {
        this.imgId = imgId;
        this.names = names;
    }

    public int getImgId() {
        return imgId;
    }

    public void setImgId(int imgId) {
        this.imgId = imgId;
    }

    public String getNames() {
        return names;
    }

    public void setNames(String names) {
        this.names = names;
    }
}
