public class MyDrawerAdapter extends BaseAdapter {

    private List<Item> mData;
    LayoutInflater mInflater;
    private Context mContext;

    public MyDrawerAdapter(List<Item> mData, Context mContext) {
        this.mData = mData;
        this.mContext = mContext;
        mInflater = LayoutInflater.from(mContext);
    }

    @Override
    public int getCount() {
        return mData.size();
    }

    @Override
    public Object getItem(int position) {
        return mData.get(position);
    }

    @Override
    public long getItemId(int position) {
        return position;
    }

    private static class ViewHolder {
        ImageView img;
        TextView tv;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {

        ViewHolder viewHolder = null;
        if (convertView == null) {
            convertView = mInflater.inflate(R.layout.item_drawerlayout, parent, false);
            viewHolder = new ViewHolder();
            viewHolder.img = (ImageView) convertView.findViewById(R.id.drawer_icon);
            viewHolder.tv = (TextView) convertView.findViewById(R.id.drawer_text);
            convertView.setTag(viewHolder);
        } else {
            convertView.getTag();
        }

        viewHolder.img.setImageResource(mData.get(position).getImgId());
        viewHolder.tv.setText(mData.get(position).getNames());

        return convertView;
    }
}
